public class Boxing {
	public static void main(String[] args) {
	 int i=100;
	 Integer i1=i;
	 System.out.println("AutoBoxed Value of int :" +i1);
	 int i2=i1;
	 System.out.println("AutoUnBoxed Value of int :" +i2);
	 float f = 100;	 
	 Float f1=f;
	 System.out.println("\n\nAutoBoxed Value of float :" +f1);
	 Float f2=f1;
	 System.out.println("AutoUnBoxed Value of float :" +f2);
	 long l=100;
	 Long l1=l;
	 System.out.println("\n\nAutoBoxed Value of long :" +l1);
	 Long l2=l1;
	 System.out.println("AutoUnBoxed Value of long :" +l2);
	 short s= 100;
	 Short s1=s;
	 System.out.println("\n\nAutoBoxed Value of short :" +s1);
	 Short s2=s1;
	 System.out.println("AutoUnBoxed Value of short :" +s2);
	 double d = 100;
	 Double d1=d;
	 System.out.println("\n\nAutoBoxed Value of double :" +d1);
	 Double d2=d1;
	 System.out.println("AutoUnBoxed Value of double :" +d2);
	 char c = 'd';
	 Character c1 = c;
	 System.out.println("\n\nAutoBoxed Value of character :" +c1);
	 Character c2 = c1;
	 System.out.println("AutoUnBoxed Value of character :" +c2);
	 byte b = 10;
	 Byte b1=b;
	 System.out.println("\n\nAutoBoxed Value of byte :" +b1);
	 Byte b2=b1;
	 System.out.println("AutoUnBoxed Value of byte :" +b2);	 
	 boolean bo=false;
	 Boolean bo1 = bo;
	 System.out.println("\n\nAutoBoxed Value of boolean :" +bo1);
	 Boolean bo2=bo1;
	 System.out.println("AutoUnBoxed Value of boolean :" +bo2);
	}
}
